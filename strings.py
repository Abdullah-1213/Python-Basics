#String Slicing
a="Hello,world"
print(a[2:5])
print(a[:5])
print(a[-1:-5])
print(a[0:7:2])

#Modify string using method
#upper
b=" Hello, world"
print(b.upper())

print(b.lower())

#remove leading whitespace
print(b.strip())

print(b.split(","))

print(b.replace("H","J"))

#concat string

a="hello"
b="jelly"
print(a+b)


#format string

a="Nasir"

print(f"hello {a} bro")

